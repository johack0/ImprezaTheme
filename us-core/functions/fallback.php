	function us_fallback_atts_us_list_filter( $atts, $content = '' ) {

		// Source format (after version 8.29)
		if ( ! empty( $atts['filter_items'] ) ) {
			$items = json_decode( rawurldecode( $atts['filter_items'] ), TRUE );
			foreach ( $items as &$item ) {
				if ( empty( $item['source'] ) OR strpos( $item['source'], '|' ) === FALSE ) {
					continue;
				}
				if ( $item['source'] == 'post|type' ) {
					$item['source'] = 'post_type';

				} elseif ( $item['source'] == 'post|author' ) {
					$item['source'] = 'post_author';

