// Carousel
if ( ! function_exists( 'us_fallback_atts_us_carousel' ) ) {
	add_filter( 'vc_edit_form_fields_attributes_us_carousel', 'us_fallback_atts_us_carousel', 710, 1 );
	add_filter( 'us_edit_atts_fallback_us_carousel', 'us_fallback_atts_us_carousel', 710, 2 );
	add_filter( 'us_fallback_atts_us_carousel', 'us_fallback_atts_us_carousel', 710, 2 );

	function us_fallback_atts_us_carousel( $atts, $content = '' ) {

		// All code below after version 8.28
		// Columns to items
		if ( ! empty( $atts['columns'] ) ) {
			$atts['items'] = $atts['columns'];
			if ( $atts['columns'] > 2 ) {
				$force_responsive_settings = TRUE;
			}
			unset( $atts['columns'] );
		}

		// Arrows
		if ( ! empty( $atts['carousel_arrows'] ) ) {
			$atts['arrows'] = 1;
			unset( $atts['carousel_arrows'] );
		}

		// Arrows style
		if ( ! empty( $atts['carousel_arrows_style'] ) ) {
			$atts['arrows_style'] = $atts['carousel_arrows_style'];
			unset( $atts['carousel_arrows_style'] );
		}

		// Arrows size
		if ( ! empty( $atts['carousel_arrows_size'] ) ) {
			$atts['arrows_size'] = $atts['carousel_arrows_size'];
			unset( $atts['carousel_arrows_size'] );
		}

		// Arrows position
		if ( ! empty( $atts['carousel_arrows_pos'] ) ) {
			$atts['arrows_pos'] = $atts['carousel_arrows_pos'];
			unset( $atts['carousel_arrows_pos'] );
		}

		// Arrows offset
		if ( ! empty( $atts['carousel_arrows_offset'] ) ) {
			$atts['arrows_offset'] = $atts['carousel_arrows_offset'];
			unset( $atts['carousel_arrows_offset'] );
		}

		// Next item offset (after version 8.28)
		if ( ! empty( $atts['carousel_items_offset'] ) ) {
			$atts['next_item_offset'] = $atts['carousel_items_offset'];
			unset( $atts['carousel_items_offset'] );
		}

		// Next item offset (after version 8.40)
		if ( ! empty( $atts['items_offset'] ) ) {
			$atts['next_item_offset'] = $atts['items_offset'];
			unset( $atts['items_offset'] );
		}

		// Dots
		if ( ! empty( $atts['carousel_dots'] ) ) {
			$atts['dots'] = 1;
			unset( $atts['carousel_dots'] );
		}

		// Center item
		if ( ! empty( $atts['carousel_center'] ) ) {
			$atts['center_item'] = 1;
			$force_responsive_settings = TRUE;
			unset( $atts['carousel_center'] );
		}

		// Slide by one
		if ( ! empty( $atts['carousel_slideby'] ) ) {
			$atts['slide_by_one'] = 0;
			unset( $atts['carousel_slideby'] );
		}

		// Loop
		// For '==' comparison with 0 and 1 always use string to avoid issues in PHP 7.4
		// https://www.php.net/manual/en/migration80.incompatible.php
		if ( isset( $atts['carousel_loop'] ) AND $atts['carousel_loop'] == '0' ) {
			$atts['loop'] = 0;
			$force_responsive_settings = TRUE;
			unset( $atts['carousel_loop'] );
		}

		// Auto height
		if ( ! empty( $atts['carousel_autoheight'] ) ) {
			$atts['autoheight'] = 1;
			$force_responsive_settings = TRUE;
			unset( $atts['carousel_autoheight'] );
		}

		// Autoplay
		if ( ! empty( $atts['carousel_autoplay'] ) ) {
			$atts['autoplay'] = 1;
			unset( $atts['carousel_autoplay'] );
		}

		// Autoplay timeout
		if ( ! empty( $atts['carousel_interval'] ) ) {
			$atts['autoplay_timeout'] = $atts['carousel_interval'];
			unset( $atts['carousel_interval'] );
		}

		// Autoplay continual
		if ( ! empty( $atts['carousel_autoplay_smooth'] ) ) {
			$atts['autoplay_continual'] = 1;
			unset( $atts['carousel_autoplay_smooth'] );
		}

		// Transition speed
		if ( ! empty( $atts['carousel_speed'] ) ) {
			$atts['transition_speed'] = $atts['carousel_speed'];
			unset( $atts['carousel_speed'] );
		}

		// Transition
		if ( ! empty( $atts['carousel_transition'] ) ) {
			$atts['transition_timing_function'] = $atts['carousel_transition'];
			unset( $atts['carousel_transition'] );
		}

		// Transition animation
		if ( ! empty( $atts['carousel_fade'] ) ) {
			$atts['transition_animation'] = 'fade';
			unset( $atts['carousel_fade'] );
		}

		// Items valign
		if ( isset( $atts['items_valign'] ) AND $atts['items_valign'] == '1' ) {
			$atts['items_valign'] = 'middle';
		}

		// Replace '%' with 'cqw' (after version 8.33)
		if ( ! empty( $atts['items_gap'] ) AND strpos( $atts['items_gap'], '%' ) !== FALSE ) {
			$atts['items_gap'] = str_replace( '%', 'cqw', $atts['items_gap'] );
		}

		// Arrows Style (after version 8.40)
		if ( isset( $atts['arrows_style'] ) AND $atts['arrows_style'] == 'block' ) {
			$atts['arrows_style'] = 'square';
		}

		// Responsive settings
		if (
			! empty( $force_responsive_settings )
			OR ! empty( $atts['breakpoint_1_width'] )
			OR ! empty( $atts['breakpoint_1_cols'] )
			OR ! empty( $atts['breakpoint_1_offset'] )
			OR isset( $atts['breakpoint_1_autoplay'] ) AND $atts['breakpoint_1_autoplay'] !== ''
			OR ! empty( $atts['breakpoint_2_width'] )
			OR ! empty( $atts['breakpoint_2_cols'] )
			OR ! empty( $atts['breakpoint_2_offset'] )
			OR isset( $atts['breakpoint_2_autoplay'] ) AND $atts['breakpoint_2_autoplay'] !== ''
			OR ! empty( $atts['breakpoint_3_width'] )
			OR ! empty( $atts['breakpoint_3_cols'] )
			OR ! empty( $atts['breakpoint_3_offset'] )
			OR isset( $atts['breakpoint_3_autoplay'] ) AND $atts['breakpoint_3_autoplay'] !== ''
		) {
			$old_responsive_settings = array(
				'1' => array(
					'width' => ! empty( $atts['breakpoint_1_width'] ) ? $atts['breakpoint_1_width'] : 'laptops',
					'cols' => ! empty( $atts['breakpoint_1_cols'] ) ? $atts['breakpoint_1_cols'] : '3',
					'offset' => ! empty( $atts['breakpoint_1_offset'] ) ? $atts['breakpoint_1_offset'] : '0px',
					'autoplay' => ( isset( $atts['breakpoint_1_autoplay'] ) AND $atts['breakpoint_1_autoplay'] !== '' ) ? $atts['breakpoint_1_autoplay'] : 1,
					'arrows' => $atts['arrows'] ?? 0,
				),
				'2' => array(
					'width' => ! empty( $atts['breakpoint_2_width'] ) ? $atts['breakpoint_2_width'] : 'tablets',
					'cols' => ! empty( $atts['breakpoint_2_cols'] ) ? $atts['breakpoint_2_cols'] : '2',
					'offset' => ! empty( $atts['breakpoint_2_offset'] ) ? $atts['breakpoint_2_offset'] : '0px',
					'autoplay' => ( isset( $atts['breakpoint_2_autoplay'] ) AND $atts['breakpoint_2_autoplay'] !== '' ) ? $atts['breakpoint_2_autoplay'] : 1,
					'arrows' => $atts['arrows'] ?? 0,
				),
				'3' => array(
					'width' => ! empty( $atts['breakpoint_3_width'] ) ? $atts['breakpoint_3_width'] : 'mobiles',
					'cols' => ! empty( $atts['breakpoint_3_cols'] ) ? $atts['breakpoint_3_cols'] : '1',
					'offset' => ! empty( $atts['breakpoint_3_offset'] ) ? $atts['breakpoint_3_offset'] : '0px',
					'autoplay' => ( isset( $atts['breakpoint_3_autoplay'] ) AND $atts['breakpoint_3_autoplay'] !== '' ) ? $atts['breakpoint_3_autoplay'] : 1,
					'arrows' => 0,
				),
			);

			$new_responsive_settings = array();
			foreach ( $old_responsive_settings as $i => $settings ) {

				$new_responsive_settings[ $i ] = array(
					'breakpoint' => in_array( $settings['width'], array( 'laptops', 'tablets', 'mobiles' ) ) ? $settings['width'] : 'custom',
					'items' => min( $atts['items'] ?? 2, $settings['cols'] ),
					'items_offset' => $settings['offset'],
					'autoplay' => $settings['autoplay'],
					'arrows' => $settings['arrows'],

					'dots' => $atts['dots'] ?? 0,
					'autoheight' => $atts['autoheight'] ?? 0,
					'loop' => $atts['loop'] ?? 1,
					'center_item' => $atts['center_item'] ?? 0,
				);
				if ( ! in_array( $settings['width'], array( 'laptops', 'tablets', 'mobiles' ) ) ) {
					$new_responsive_settings[ $i ]['breakpoint_width'] = $settings['width'];
				}
			}

			$atts['responsive'] = rawurlencode( json_encode( $new_responsive_settings ) );

			// Unset old responsive settings
			foreach ( array( 1, 2, 3 ) as $i ) {
				if ( isset( $atts["breakpoint_{$i}_width"] ) ) {
					unset( $atts["breakpoint_{$i}_width"] );
				}
				if ( isset( $atts["breakpoint_{$i}_cols"] ) ) {
					unset( $atts["breakpoint_{$i}_cols"] );
				}
				if ( isset( $atts["breakpoint_{$i}_offset"] ) ) {
					unset( $atts["breakpoint_{$i}_offset"] );
				}
				if ( isset( $atts["breakpoint_{$i}_autoplay"] ) ) {
					unset( $atts["breakpoint_{$i}_autoplay"] );
				}
			}
		}

		return $atts;
	}
}

