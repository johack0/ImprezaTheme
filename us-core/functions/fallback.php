				if (
					isset( $item['source'] )
					AND $item['source'] === 'us_portfolio_category'
					AND $custom_cat_slug = us_get_option( 'portfolio_category_slug', '' )
					AND $item['source'] !== 'us_' . $custom_cat_slug
				) {
					$item['source'] = $custom_cat_slug;
				}
				if (
